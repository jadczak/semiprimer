import unittest
from semiprimer.semiprimer import SemiPrime

# RSA 100
# 1522605027922533360535618378132637429718068114961380688657908494580122963258952897654000350692006139
#   37975227936943673922808872755445627854565536638199
# Ã— 40094690950920881030683735292761468389214899724061


class MyTestCase(unittest.TestCase):
    def test_rsa_100(self):
        rsa_100 = "1522605027922533360535618378132637429718068114961380688657908494580122963258952897654000350692006139"
        factor_1 = 37975227936943673922808872755445627854565536638199
        factor_2 = 40094690950920881030683735292761468389214899724061
        semiprime = SemiPrime(rsa_100)
        semiprime.factor()
        self.assertIn(factor_1, semiprime.factors)
        self.assertIn(factor_2, semiprime.factors)


if __name__ == "__main__":
    unittest.main()
